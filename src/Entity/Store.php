<?php

namespace App\Entity;

use App\Repository\StoreRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=StoreRepository::class)
 */
class Store
{
    public function __construct()
    {
        $this->setCreatedAt(new \DateTime('now'));
        $this->setHash(uniqid());
        $this->user = new ArrayCollection();
        $this->store = new ArrayCollection();
    }

    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=100)
     */
    private $domain;

    /**
     * @ORM\Column(type="string", length=15)
     */
    private $hash;

    /**
     * @ORM\OneToMany(targetEntity="User", mappedBy="store")
     *
     */
    private $user;

    /**
     * @ORM\Column(type="datetime")
     */
    private $createdAt;

    /**
     * @ORM\OneToMany(targetEntity="Product", mappedBy="store")
     */
    private $store;


    public function getId(): ?int
    {
        return $this->id;
    }

    public function getDomain(): ?string
    {
        return $this->domain;
    }

    public function setDomain(string $domain): self
    {
        $this->domain = $domain;

        return $this;
    }

    public function getUser(): ?User
    {
        return $this->user;
    }

    public function setUser(?User $user): self
    {
        $this->user = $user;

        return $this;
    }

    public function getCreatedAt(): ?\DateTimeInterface
    {
        return $this->createdAt;
    }

    public function setCreatedAt(\DateTimeInterface $createdAt): self
    {
        $this->createdAt = $createdAt;

        return $this;
    }

    public function getHash(): ?string
    {
        return $this->hash;
    }

    public function setHash(string $hash): self
    {
        $this->hash = $hash;

        return $this;
    }

    public function addUser(User $user): self
    {
        if (!$this->user->contains($user)) {
            $this->user[] = $user;
            $user->setStore($this);
        }

        return $this;
    }

    public function removeUser(User $user): self
    {
        if ($this->user->contains($user)) {
            $this->user->removeElement($user);
            // set the owning side to null (unless already changed)
            if ($user->getStore() === $this) {
                $user->setStore(null);
            }
        }

        return $this;
    }

    /**
     * @return Collection|Product[]
     */
    public function getStore(): Collection
    {
        return $this->store;
    }

    public function addStore(Product $store): self
    {
        if (!$this->store->contains($store)) {
            $this->store[] = $store;
            $store->setStore($this);
        }

        return $this;
    }

    public function removeStore(Product $store): self
    {
        if ($this->store->contains($store)) {
            $this->store->removeElement($store);
            // set the owning side to null (unless already changed)
            if ($store->getStore() === $this) {
                $store->setStore(null);
            }
        }

        return $this;
    }
}
